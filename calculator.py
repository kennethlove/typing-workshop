def add(num1: complex, num2: complex) -> complex:
    return num1 + num2


def subtract(num1: complex, num2: complex) -> complex:
    return num1 - num2


def multiply(num1: complex, num2: complex) -> complex:
    return num1 * num2


def divide(num1: complex, num2: complex) -> complex:
    return num1 / num2

